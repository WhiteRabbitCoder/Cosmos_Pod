# Se solicita al usuario que ingrese los grados Celsius y se convierte a número decimal (float)
celsius = float(input("Ingrese los grados Celsius: "))

# Se aplica la fórmula para convertir de Celsius a Fahrenheit
fahrenheit = (celsius * 9/5) + 32

# Se muestra el resultado en pantalla
print(f"Los grados Fahrenheit equivalentes son: {fahrenheit}")